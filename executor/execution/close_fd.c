#include "libft.h"
#include "strict.h"
#include "test.h"

void	close_fd(int *fd)
{
	if (fd[0] != -1 && fd[0] != 0)
	{
		strict_close(fd[0]);
		fd[0] = -1;
	}
	if (fd[1] != -1 && fd[1] != 1)
	{
		strict_close(fd[1]);
		fd[1] = -1;
	}
}
